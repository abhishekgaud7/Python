# Escape Sequence Characters in Python

print("Hello\tWorld")  # Tab space between Hello and World
print("Hello\nWorld")  # New line between Hello and World
print("Hello\\World")  # Backslash character
print("Hello\'World")  # Single quote character
print("Hello\"World")  # Double quote character
print("Hello\rWorld")  # Carriage return
print("Hello\bWorld")  # Backspace character
print("Hello\fWorld")  # Form feed character
print("Hello\vWorld")  # Vertical tab character
print("Hello World")   # Normal string without escape sequences
print("C:\\Users\\lenovo\\Documents")  # File path example
print("This is a \"quoted\" word.")  # Quoted word within a string
print('It\'s a beautiful day!')  # Single quote within a string
print("First Line.\nSecond Line.\nThird Line.")  # Multiple lines
print("Column1\tColumn2\tColumn3")  # Tabbed columns
print("Backslash: \\")  # Displaying a backslash
print("Carriage Return Example\rOverwrite")  # Carriage return example
print("Form Feed Example\fNext Page")  # Form feed example
print("Vertical Tab Example\vNext Line")  # Vertical tab example
print("End of Escape Sequences Demo.")
